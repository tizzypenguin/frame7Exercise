  created() {},
  beforeMount() {},
  mounted() {},
  beforeUpdate() {},
  updated() {},
  beforeDestroy() {},
  destroyed() {},
  methods: {
    openIndicator() {
      const self = this;
      self.$f7.preloader.show();
      setTimeout(() => {
        self.$f7.preloader.hide();
      }, 2000);
    },
    openDialog() {
      const self = this;
      self.$f7.dialog.preloader();
      setTimeout(() => {
        self.$f7.dialog.close();
      }, 2000);
    },
    openCustomDialog() {
      const self = this;
      self.$f7.dialog.preloader('My text...');
      setTimeout(() => {
        self.$f7.dialog.close();
      }, 2000);
    }
  }
};
</script>

